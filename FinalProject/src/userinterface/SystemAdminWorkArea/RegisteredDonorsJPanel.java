    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) DonorDetailsTable.getModel();
        model.setRowCount(0);
       
        for(Donor donor : system.getMasterDonorDirectory()){
            Object[] row = new Object[2];
            row[0] = donor;
            row[1] = donor.getDonorName();
            
            model.addRow(row);
        }
        
    }

